#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	unsigned int	a;

	a = 0;
	if (n == 0)
		return (s1);
	if (!s2)
	{
		s1[a] = '\0';
		return (s1);
	}
	while (s2[a] && a < n)
	{
		s1[a] = s2[a];
		a++;
	}
	if (a < n)
		s1[a] = '\0';
	return (s1);
}
