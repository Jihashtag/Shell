#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;

	i = 0;
	if (b != NULL)
		while (i < len)
		{
			((char *)b)[i] = (char)c;
			i++;
		}
	return (b);
}
