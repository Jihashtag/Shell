#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	a;

	a = 0;
	if (s)
	{
		while (s[a])
		{
			f(a, &((char *)s)[a]);
			a++;
		}
	}
}
